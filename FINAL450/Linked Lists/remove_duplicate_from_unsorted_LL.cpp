class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
        set<int> all_nodes_data;
        Node * prev = NULL;
        Node * curr = head;
        while(curr != NULL)
        {
            if(all_nodes_data.find(curr->data) == all_nodes_data.end())
            {
                all_nodes_data.insert(curr->data);
                prev = curr;
                curr = curr->next;
            }
            else
            {
                Node * temp = curr->next;
                prev->next = temp;
                curr = temp;
            }
        }
        return head;
    }
};


